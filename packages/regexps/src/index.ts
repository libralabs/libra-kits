/** 手机号正则 */
export const phone = /^(\+?0?86\-?)?1[3456789]\d{9}$/;

/** 邮箱正则 */
export const email = /\w+([-+.]\w+)*@\w+([-.]\w+)*\.\w+([-.]\w+)*/;

/** URL正则 */
export const url =
  /[-a-zA-Z0-9@:%._\+~#=]{2,256}\.[a-z]{2,6}\b([-a-zA-Z0-9@:%_\+.~#?&//=]*)/i;

/** 身份证号 */
export const idCard =
  /^[1-9]\d{5}(\d{4})(0[1-9]|1[0-2])([0-2][1-9]|10|20|30|31)\d{2}(\d)[0-9Xx]$/;
